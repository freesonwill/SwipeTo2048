package com.freesonwill.swipeTo2048.model;

public class Cell {
    protected final int x, y;

    public Cell(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getValue() {
        return value;
    }

    protected int value;


}
